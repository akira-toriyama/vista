/**
 * Codegen: `furrow schema task` (JSON Schema, single source of truth in the
 * furrow repo) → src/domain/generated/.
 *
 * Emits two committed artifacts:
 *   - furrow.task.schema.json  — vendored snapshot of the schema we generated
 *     from. The contract test diffs it against the live `furrow schema task`
 *     output so drift fails CI instead of rotting silently.
 *   - furrow-task.ts           — the TS type for one task shard.
 *
 * Run with: pnpm codegen  (requires furrow on PATH — the source wrapper)
 */
import { execFile } from "node:child_process";
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { promisify } from "node:util";
import { compile } from "json-schema-to-typescript";

const run = promisify(execFile);
const outDir = join(dirname(fileURLToPath(import.meta.url)), "../src/domain/generated");

const { stdout } = await run("furrow", ["schema", "task"]).catch((e) => {
  console.error(`codegen requires furrow on PATH: ${e.message}`);
  process.exit(1);
});
const schema = JSON.parse(stdout);

const banner = `/**
 * GENERATED by scripts/codegen-furrow-types.mjs — do not edit.
 * Source of truth: \`furrow schema task\` (${schema.$id ?? "furrow"}).
 * Regenerate with: pnpm codegen
 */`;

const ts = await compile(schema, "FurrowTaskShard", {
  bannerComment: banner,
  additionalProperties: true,
});

await mkdir(outDir, { recursive: true });
await writeFile(join(outDir, "furrow.task.schema.json"), JSON.stringify(schema, null, 2) + "\n");
await writeFile(join(outDir, "furrow-task.ts"), ts);
console.log(`wrote ${outDir}/{furrow.task.schema.json,furrow-task.ts}`);
